def func(num1, num2):
    """
    Returns the sum of num1 and num2 multiplied by 2.

    Parameters:
    num1 (int): The first number to be added.
    num2 (int): The second number to be added.

    Returns:
    int: The sum of num1 and num2 multiplied by 2.
    """
    return (num1 + num2) * 2

def main():
    result = func(3, 4)
    print(result)

if __name__ == "__main__":
    main()
